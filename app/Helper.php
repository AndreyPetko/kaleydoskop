<?php

namespace App;

class Helper {
	public static function create_item($type, $request) {
		unset($request['_token']);
		$type->create($request);
	}


	public static function isFilterEmpty($filter) {
		if(empty($filter)) {
			return 1;
		}
		$count = 0;
		foreach ($filter as $filterItem) {
			if(empty($filterItem)) {
				$count++;
			}
		}
		if($count == count($filter)) {
			return 1;
		}

		return 0;
	}


